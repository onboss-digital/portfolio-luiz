// Translation functionality
let currentLanguage = 'pt';

function translatePage(language) {
    currentLanguage = language;
    const elements = document.querySelectorAll('[data-translate]');

    // BUGFIX: Preserve current theme when changing language
    const currentTheme = document.documentElement.classList.contains('dark') ? 'dark' : 'light';

    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[language] && translations[language][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[language][key];
            } else {
                element.textContent = translations[language][key];
            }
        }
    });

    // Update document title
    document.title = translations[language].page_title;

    // Update HTML lang attribute
    document.documentElement.lang = language === 'pt' ? 'pt-BR' : language;

    // BUGFIX: Restore theme after language change
    if (currentTheme === 'dark') {
        document.documentElement.classList.add('dark');
    } else {
        document.documentElement.classList.remove('dark');
    }

    // Store language preference
    localStorage.setItem('preferredLanguage', language);
}

// Initialize language system
function initLanguageSystem() {
    // Check for stored language preference - DEFAULT CHANGED TO ENGLISH
    const storedLanguage = localStorage.getItem('preferredLanguage') || 'en';

    // Set up language selector
    const languageSelector = document.getElementById('language-selector');
    if (languageSelector) {
        languageSelector.value = storedLanguage;
        languageSelector.addEventListener('change', (e) => {
            translatePage(e.target.value);
        });
    }

    // Translate to stored language
    translatePage(storedLanguage);
}

// Export for use in other scripts
window.translatePage = translatePage;
window.initLanguageSystem = initLanguageSystem;
window.currentLanguage = () => currentLanguage;